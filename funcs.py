def addition(a, b):
    """
    Функция для сложения двух чисел

    Args:
        a (int): Первое число
        b (int): Второе число

    Returns:
        int: Результат сложения a и b
    """
    return a + b

def subtraction(a, b):
    """
    Функция для вычитания двух чисел

    Args:
        a (int): Первое число
        b (int): Второе число

    Returns:
        int: Результат вычитания b из a
    """
    return a - b